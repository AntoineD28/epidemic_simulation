    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean spac, pp;
        boolean confinement = false, masque = false, quarantaine = false, vaccination = false;
        int N = 1, M = 1, tQuarantaine = 0;
        double pVaccin = 0;
        
        String Modele = jComboBox1.getSelectedItem().toString();
        
        double beta = jSlider1.getValue();
        beta = beta/100;
        
        double gamma = jSlider2.getValue();
        gamma = gamma/100;
        
        double alpha = jSlider3.getValue();
        alpha = alpha/100;
        
        double mu = jSlider4.getValue();
        mu = mu/100;
        
        double eta = jSlider5.getValue();
        eta = eta/100;
        
        int temps = (int)jSpinner2.getValue();
        
        int s = (int)jSpinner1.getValue();

        int e = (int)jSpinner3.getValue();
        
        int i = (int)jSpinner4.getValue();
        
        int r = (int)jSpinner5.getValue();

        if (jRadioButton1.isSelected()){
            spac = true;
            N = (int)jSpinner7.getValue();
            M = (int)jSpinner6.getValue();
        }
        else
            spac = false;
        
        if (jRadioButton3.isSelected()){
            pp = true;    
            if (jCheckBox1.isSelected())
                confinement = true;
            if (jCheckBox2.isSelected())
                masque = true;
            if (jCheckBox1.isSelected()){
                quarantaine = true;
                tQuarantaine = (int)jSpinner8.getValue();
            }
            if (jCheckBox1.isSelected())
                vaccination = true;
                pVaccin = (double)jSlider6.getValue()/100;
        }
        else
            pp = false;
       
        
        System.out.println("\n\n\n\n-------------RESULTAT SIMULATION-------------");
        if (Modele.equals("SIR")){
            Sir s1 = new Sir(beta,gamma,s,i,r,temps,N,M,confinement,masque,quarantaine,vaccination,tQuarantaine,pVaccin);
            s1.simulation();
            Object[][] data = s1.getData();
            System.out.println(data.length);
            for(int j = 0; j<temps; j++){
                for(int k = 0; k<6; k++){
                    jTable2.setValueAt(data[j][k],j,k);
                }
            } 
        }
        
        if (Modele.equals("SEIR")){
            Seir s2 = new Seir(beta,alpha,gamma,s,e,i,r,temps,N,M,confinement,masque,quarantaine,vaccination,tQuarantaine,pVaccin);
            s2.simulation();
        }
        if (Modele.equals("SEIRN")){
            SeirN s3 = new SeirN(beta,alpha,gamma,mu,eta,s,e,i,r,temps,N,M,confinement,masque,quarantaine,vaccination,tQuarantaine,pVaccin);
            s3.simulation();
        }

        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

